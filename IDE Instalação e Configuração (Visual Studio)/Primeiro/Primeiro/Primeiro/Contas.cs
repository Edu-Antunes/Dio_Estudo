using System;
using System.Collections.Generic;
using System.Text;

namespace Contas

{
    public class Contas
    {
        public int Soma(int a, int b)
        {
            return a + b;
        }

        public int Subtracao(int a, int b)
        {
            return a - b;
        }

        public int Divicao(int a, int b)
        {
            return a / b;
        }
    

        public int Multiplicacao(int a, int b)
        {
        return a * b;
        }

}
}



